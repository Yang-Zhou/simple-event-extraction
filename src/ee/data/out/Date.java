package ee.data.out;

/**
 * The date of the template
 */
public class Date {
	public String year;
	public String month;
	public String day;
	public String hour;
	public String minute;
	public String second;

	public Date(String year, String month, String day, String hour,
			String minute, String second) {
		super();
		this.year = year;
		this.month = month;
		this.day = day;
		this.hour = hour;
		this.minute = minute;
		this.second = second;
	}

}
